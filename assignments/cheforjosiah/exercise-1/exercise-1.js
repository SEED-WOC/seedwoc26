// Exercise 1: Variable declaration and data types

// Declaring variable and setting data into them
let name = "Josiah";
let age = 22;
let height = 1.72;
let isEmployed = false;


// Declaring variables for saving to type of the above variables
let nameType = typeof(name);
let ageType = typeof(age);
let heightType = typeof(height);
let isEmployedType = typeof(height);

// Displaying the data and type to the console.
console.log(`Name: ${name}, Type: ${nameType}`);
console.log(`Age: ${age}, Type: ${ageType}`);
console.log(`Height: ${height}, Type: ${heightType}`);
console.log(`Is Employed?: ${isEmployed}, Type: ${isEmployedType}`);