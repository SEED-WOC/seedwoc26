// Exercise 2: Arithmetic Operations Calculator

// Variables saving data values for arithmetic.
const num1 = 6;
const num2 = 2;

// Creating variables for saving results of arithmetic operations into.
const addition = num1 + num2;
const subtraction = num1 - num2;
const multiplication = num1 * num2;
const division = num1 / num2;
const modulus = num1 % num2;
const exponential = num1 ** num2;

// Displaying the numbers for arithmetic, the arithmetic operation done and the results
console.log(`${num1} + ${num2} = ${addition}`);
console.log(`${num1} - ${num2} = ${subtraction}`);
console.log(`${num1} * ${num2} = ${multiplication}`);
console.log(`${num1} / ${num2} = ${division}`);
console.log(`${num1} % ${num2} = ${modulus}`);
console.log(`${num1} ** ${num2} = ${exponential}`);